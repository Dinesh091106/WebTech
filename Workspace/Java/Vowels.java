class Vowels
{
	public static void main(String[]args)
	{
		int vowels='N';
		switch(vowels)
		{
			case 'A':System.out.println("1st vowels");
			break;
			case 'E':System.out.println("2nd vowels");
			break;
			case 'I':System.out.println("3rd vowels");
			break;
			case 'O':System.out.println("4th vowels");
			break;
			case 'U':System.out.println("5th vowels");
			break;
			default:System.out.println("Invalid");
		}
	}
}